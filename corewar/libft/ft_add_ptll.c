#include "libft.h"

t_ptll		ft_add_ptll(t_ptll pt1, t_ptll pt2)
{
	t_ptll		pt3;

	pt3.x = pt1.x + pt2.x;
	pt3.y = pt1.y + pt2.y;
	return (pt3);
}
