#include "libft.h"

int		ft_nblen(int n)
{
	int len;

	len = 0;
	if (n < 0)
	{
		n = -n;
		len = ft_nblen(n);
		len++;
	}
	else if (n >= 10)
	{
		len = ft_nblen(n / 10);
		len++;
	}
	else
		len++;
	return (len);
}
