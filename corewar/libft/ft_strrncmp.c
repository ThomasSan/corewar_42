#include "libft.h"

int		ft_strrncmp(char *s1, char *s2, int n)
{
	int		i1;
	int		i2;

	i1 = ft_strlen(s1);
	i2 = ft_strlen(s2);
	while (s1[i1] == s2[i2] && i1 >= 0 && i2 >= 0 && n--)
	{
		i1--;
		i2--;
	}
	return (s1[i1] - s2[i2]);
}
