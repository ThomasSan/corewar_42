#include "libft.h"

t_pt	ft_add_pt(t_pt pt1, t_pt pt2)
{
	t_pt pt3;

	pt3.x = pt1.x + pt2.x;
	pt3.y = pt1.y + pt2.y;
	return (pt3);
}
