#include "libft.h"

int		ft_ishexa(int c)
{
	if (((c >= 'A' && c <= 'F') || (c >= 'a' && c <= 'f')) || ft_isdigit(c))
		return (_SUCCESS_);
	return (_ERROR_);
}
