#include "libft.h"

t_ptd3d		ft_add_ptd3d(t_ptd3d pt1, t_ptd3d pt2)
{
	t_ptd3d		pt3;

	pt3.x = pt1.x + pt2.x;
	pt3.y = pt1.y + pt2.y;
	pt3.z = pt1.z + pt2.z;
	return (pt3);
}
