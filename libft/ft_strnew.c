#include "libft.h"
#include <stdlib.h>

char	*ft_strnew(size_t size)
{
	char	*str;

	str = (char*)malloc(sizeof(str) * size);
	if (!str)
		return (NULL);
	ft_memset(str, 0, size);
	return (str);
}
