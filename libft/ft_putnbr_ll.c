#include "libft.h"
#include <unistd.h>

void	ft_putnbr_ll(long long n)
{
	if (n == -9223372036854775807 - 1)
		return (ft_putstr("-9223372036854775808"));
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if (n >= 10)
	{
		ft_putnbr_ll(n / 10);
		ft_putnbr_ll(n % 10);
	}
	else
		ft_putchar(n + 48);
}
